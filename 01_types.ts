// implicitly typed variable
let impNum = 10;
let impStr = 'Hello';
let impBool = true;
let impArrNum = [1, 2, 3];
let impArrStr = ['Hello', 'World'];
let impArrAny = [1, 'Hello', true];

// explicitly typed variable
let expNum: number = 10;
let expStr: string = 'Hello';
let expBool: boolean = true;
let expArrNum: number[] = [1, 2, 3];
let expArrStr: string[] = ['Hello', 'World'];
let expArrAny: any[] = [1, 'Hello', true];

//any
let a: any = 10;
a = 'Hello';
a = true;
a = [1, 2, 3];
a = ['Hello', 'World'];
a = [1, 'Hello', true];

